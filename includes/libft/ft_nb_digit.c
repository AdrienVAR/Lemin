#include "libft.h"

int	ft_nb_digit(int n)
{
	int i;

	i = 0;
	if (n < 0)
		n = -n;
	if (n == -2147483648)
		return (10);
	if (n == 0)
		return (1);
	while (n > 0)
	{
		i++;
		n = n / 10;
	}
	return (i);
}
