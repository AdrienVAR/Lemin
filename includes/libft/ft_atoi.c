#include "libft.h"

int	ft_atoi(const char *str)
{
	int					i;
	int					atoisign;
	unsigned long long	nb;

	atoisign = 1;
	nb = 0;
	i = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' ||
			str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			atoisign = atoisign * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + str[i] - 48;
		i++;
	}
	if (i > 19 || nb > 9223372036854775807)
		return (atoisign < 0 ? 0 : -1);
	return ((int)nb * atoisign);
}
